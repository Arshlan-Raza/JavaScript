let userInput = prompt('Tell me a fruit');


switch(userInput){
    case 'Orange':
        alert('Orange is best for energy');
        break;
    case  'Apple':
        alert("A apple a day keeps the doctor away!");
        break;
    case 'Grapes':
        alert('Grapes juices are good');
        break;
    default:
        alert("Never heard of that fruit");
        break;

}